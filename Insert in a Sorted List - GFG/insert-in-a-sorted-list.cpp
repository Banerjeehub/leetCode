class Solution{
  public:
    // Should return head of the modified linked list
    Node *sortedInsert(struct Node* head, int data) {
        Node* temp = head;
        if(data < head->data)
        {
            Node* dummy = new Node(data);
            dummy->next = head;
            head = dummy;
            return head;
        }
        
        while(temp->next && data > temp->next->data)
        {
            temp = temp->next;
        }
        
        if(temp->next == NULL)
        {
            Node* dummy = new Node(data);
            temp->next = dummy;
            return head;
        }
        
        Node* dummy = new Node(data);
        Node* connect = temp->next;
        
        temp->next = dummy;
        dummy->next = connect;
        
        return head;
    }
};


